def _focus_index(question: str) -> int:
    q = (question or "").strip()
    if not q:
        return 0
    h = int(hashlib.md5(q.encode("utf-8")).hexdigest(), 16)
    return h % len(FOCUS_VARIANTS)


def build_system_prompt(question: str = "") -> str:
    """단일 최상 품질 모드. 질문별로 강조 관점만 바뀐다."""
    focus = FOCUS_VARIANTS[_focus_index(question)]
    parts = [
        PERSONA_OPENING,
        "",
        RULES_PRIORITY.strip(),
        "",
        RULES_QUOTE_SUMMARY.strip(),
        "",
        RULES_BODY_STRUCTURE.strip(),
        "",
        RULES_CORE.rstrip(),
        "",
        "[이번 답변에서 추가로 살필 관점]",
        focus,
    ]
    return "\n".join(parts)


def build_user_prompt(question: str, context: str, reference_date: date | None = None) -> str:
    """난이도 없이 항상 깊이 있는 구조를 요구한다."""
    ref = reference_date or date.today()
    return (
        f"질문 기준일: {ref.isoformat()} (이 날짜를 「질문 시점」으로 두고 (4)번 날짜 규칙을 적용한다.)\n\n"
        f"질문: {question}\n\n"
        "위 질문에 **특정 인물 이름·직함**만 있고 ‘비교’·‘여야’·‘타 위원 포함’ 같은 요청이 없으면 **단일인 질문**으로 간주한다:\n"
        "- `## 세부 근거` **모든 불릿**은 **그 사람만 주어**로 작성한다 (**다른 위원·다른 의원·한편 타인 불릿 0개**).\n"
        "- 그 사람 직접 발언 청크가 부족하면 불릿을 억지로 늘리지 말고 `## 한계`에 적는다.\n\n"
        f"[컨텍스트]\n{context}\n\n"
        "서식(필수): 답변은 **Markdown**으로 작성한다.\n"
        "- 본문의 `##` 헤더·섹션 순서는 시스템 프롬프트 「본문 구조」와 동일하게 맞춘다.\n"
        "- `## 세부 근거`에서는 불릿 `-`를 쓰고, 한 번에 한 줄에 모든 분석을 몰아넣지 않는다.\n"
        "- 중요 용어는 **굵게** 처리할 수 있다.\n\n"
        "인용 규칙:\n"
        "- 본문에서 주장마다 문장 끝 또는 바로 뒤에 [n] 또는 [n][m] 형식으로 각주를 붙일 것\n"
        "- [n]은 아래 컨텍스트에 부여된 번호와 반드시 일치할 것\n"
        "- [ref: ...] 같은 다른 인용 형식은 금지\n\n"
        "(4)번(2025년 이후 회의·365일 이상 시차·날짜 확인 필요)에 따라 안내 문장이 필요하면 "
        "`## 한계` 다음이나 답변 맨 마지막에 한 줄로 둔다."
    )
