        "   - NO  → `## 메인 결과`에 ‘회의록에서 해당 내용은 확인되지 않았습니다’만 작성, `## 세부 근거` **완전 금지**\n"
        + _e_item
        + f"[컨텍스트]\n{context}\n\n"
        "서식(필수): 답변은 **Markdown**으로 작성한다.\n"
        "- 순서 고정: `## 메인 결과` → `## 세부 근거` → `## 확인된 범위`(조건부). `##` 형식 필수.\n"
